const GUID* zaryaProxyAllowFilterV4Key(int index)
{
    if (index < 0 || index >= kMaxProxyAllowFilters) {
        return nullptr;
    }
    return &kProxyAllowV4Keys[index];
}

const GUID* zaryaProxyAllowFilterV6Key(int index)
{
    if (index < 0 || index >= kMaxProxyAllowFilters) {
        return nullptr;
    }
    return &kProxyAllowV6Keys[index];
}

int zaryaMaxProxyAllowFilters()
{
    return kMaxProxyAllowFilters;
}

void zaryaAllZaryaFilterKeys(GUID* keys, int* count)
{
    int index = 0;
    keys[index++] = ZARYA_FILTER_ALLOW_LOOPBACK_V4;
    keys[index++] = ZARYA_FILTER_ALLOW_LOOPBACK_V6;
    keys[index++] = ZARYA_FILTER_ALLOW_TUN_IFACE_V4;
    keys[index++] = ZARYA_FILTER_ALLOW_TUN_IFACE_V6;
    for (int proxyIndex = 0; proxyIndex < kMaxProxyAllowFilters; ++proxyIndex) {
        keys[index++] = kProxyAllowV4Keys[proxyIndex];
        keys[index++] = kProxyAllowV6Keys[proxyIndex];
    }
    keys[index++] = ZARYA_FILTER_BLOCK_OUTBOUND_V4;
    keys[index++] = ZARYA_FILTER_BLOCK_OUTBOUND_V6;
    if (count) {
        *count = index;
    }
}

